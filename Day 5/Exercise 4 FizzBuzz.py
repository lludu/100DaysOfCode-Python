#Exercise 2 - Adding all even numbers between 1 to 100:


for number in range(1, 101):
  if (number % 15 == 0):
    print('FizzBuzz')
  elif (number % 3 == 0):
    print('Fizz')
  elif (number % 5 == 0):
    print('Buzz')
  else:
    print(number)




